        <!-- zdjecie pionier -->
        <h2 class="headerHistory">Pionier</h2>
        <p>W 1946 roku z Przemyśla do Wrocławia zostaje przeniesiona Oficerska Szkoła Saperów. Przy niej, 18 marca 1946 roku na zebraniu założycielskim zostaje powołany do życia <span class="green"> Klub Sportowy Pionier </span>. Zostają założone sekcje piłki nożnej, hokeja, boksu i wioślarstwa. Pionier Wrocław występy rozpoczął od B-klasy dolnośląskiej. Sezon 1946/47 zakończył na pierwszym miejscu i awansował do A-klasy.</p>
        
      </div>


      <div class="col-lg-4">
        <!-- zdjecie podchorazak -->
        <h2 class="headerHistory">Podchorążak</h2>
        <p>W 1947 roku z Krakowa do Wrocławia przeniosła się Oficerska Szkoła Piechoty Nr 1. Szkoła ta powstała w czerwcu 1943 roku w Riazaniu w ZSRR jako Dywizyjna Szkoła Podchorążych, a w roku 1945 została przeniesiona do Krakowa. Przy szkole powstał <span class="green"> Klub Sportowy Podchorążak. </span> Data powstania klubu nie jest znana, na pewno funkcjonował on już w Krakowie.</p>
      </div>

      <div class="col-lg-4">
       <!-- logo wks ślask wroclaw -->
